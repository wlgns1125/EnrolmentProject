        if(중복여부 == true){
            JOptionPane.showMessageDialog(null, "신청강의시간이 중복되었습니다.");
            return;
        }
        
        table.addRow(row1);
        총학점 = 총신청학점();
        jTable1.setValueAt(총학점, 0, 4);
        테이블1채우기();
