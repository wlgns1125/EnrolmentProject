        if(추가여부 == true){
            JOptionPane.showMessageDialog(null, "이미 등록된 과목입니다.");
            return;
        }
        
        int 총학점 = 총신청학점();
        if((총학점 + Integer.parseInt(row1[4].toString())) > 가능학점)
        {
            JOptionPane.showMessageDialog(null, "신청가능학점이 초과되었습니다.");
            return;
        }

